const generateDates = () => {
  const dates: string[] = [];
  const today = new Date("2024-01-14");
  for (let i = 0; i < 7; i++) {
    const date = new Date(today);
    date.setDate(date.getDate() + i);
    dates.push(date.toISOString().split("T")[0]);
  }
  return dates;
};

